    CALL MPI_GATHER(PSENDBUF,ISENDCOUNT,INT(MPI_REAL8),PRECVBUF(1),&
     & ISENDCOUNT,INT(MPI_REAL8),IROOT-1,ICOMM,IERROR)
  ENDIF
